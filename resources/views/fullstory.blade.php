        <p class="indent-8">В 1972 году церковный староста Ярошук Владимир Евфимович нашел в разоренной церкви опись Церковного имущества и опись имущества вывезенного в Россию в 1915 году, составленную о. Никанором Котовичем.
        </p>
        <p class="indent-8">Поруганное здание церкви власти отдали местному колхозу под амбар. Чтобы навечно стереть память о Крупчицкой церкви, о Крупчицком сражении, богоборческие власти предприняли следующий шаг. Решением Жабинковского райисполкома три деревни: Крупчицы, Чижевщина и Рыковичи объединили в одну с общим названием Чижевщина. Уже разоренная, Св.Владимировская церковь для отдельных граждан являлась камнем преткновения. Житель д. Крупчицы, проходя мимо опустошенной церкви заметил дым, выходящий из окон церкви. По телефону он вызвал пожарных. Прибывшие спасатели обнаружили, что внутри церкви на полу горел костер, при том что на входных дверях висел замок. Пожар был потушен. Кресты, установленные высоко на куполах остались целыми, а на притворах на небольшой высоте поломали.
        </p>
        <h3 class="text-2xl py-2">12. Возрождение Святыни.</h3>
        <p class="indent-8 py-2">Перестройка в СССР разрушила экономику, но для церквей наступило благоприятное время. На бывшем Крупчицком приходе остались люди не равнодушные к своей церкви. Одним с таковых являлся Ярошук Владимир Евфимович. Еще в 1988году он обращался в различные инстанции о передаче здания Крупчицкой церкви верующим.
        </p>
        <p class="indent-8 py-2">
            <img src="{{asset('images/doc.jpg')}}" class="#"/>
        </p>
        <p class="indent-8 py-2">
            <img src="{{asset('images/doc2.jpg')}}" class="#"/>
        </p>
        <p class="indent-8 py-2">Такое письмо было написано с указанием большого значения Крупчицкой церкви не только для верующих, но как значимого исторического памятника. Это письмо было отправлено в Москву академику Дмитрию Лихачеву. Как свидетельствуют представленные документы, власти без особого энтузиазма стали заниматься восстановлением храма, но и не препятствовали его восстановлению. Усилиями В.Е.Ярошука, В.А.Карпинчика  Г.Куличика, Ф.Андреюка и других под руководством настоятеля прихода Иоанна Поддубного, (на приходе с 1990 года) который сам покрывал купола, ремонтные работы были завершены в 1992 году.
        </p>
        <p class="indent-8 py-2">
            <img src="{{asset('images/doc3.jpg')}}" class="#"/>
        </p>
        <p class="indent-8 py-2">
            <img src="{{asset('images/doc4.jpg')}}" class="#"/>
        </p>
        <p class="indent-8 py-2">
            <img src="{{asset('images/doc5.jpg')}}" class="#"/>
        </p>
        <p class="indent-8 py-2">Указом Архиепископа Брестского и Кобринского Константина было назначено освящение Крупчицкой церкви перед началом восстановительных работ. Освящение совершил 28 июля 1990 года настоятель Св.Симеоновского собора г.Бреста, секретарь Брестской епархии Митрофорный протоиерей Евгений Парфенюк (на фото в центре) в сослужении священников Св.Симеоновского собора, священников Жабинковского и Кобринского благочиний в присутствии большого количества прихожан, гостей и паломников из других приходов ( около 2-х тысяч). На фото: справа от Евгения Парфенюка благочинный Жабинковского округа, настоятель Мыщицкой Св.Покровской церкви Митрофорный протоиерей  Николай Черкасевич. Слева – благочинный Кобринского округа Митрофорный протоиерей Евгений Кушнир. Сзади – настоятель Св.Владимировской церкви протоиерей Иоанн Поддубный.
        </p>
        <p class="indent-8 py-2">
            <img src="{{asset('images/doc6.jpg')}}" class="#"/>
        </p>
        <p class="indent-8 py-2">
            <img src="{{asset('images/doc7.jpg')}}" class="#"/>
        </p>
        <p class="indent-8 py-2">Первоочередной задачей было сбор средств на реставрацию. Необходимо было выполнить: 1.Замену негодной кровли. 2. Изготовление и покрытие куполов с установкой крестов. 3.Устройство полов. 4.Ремонт штукатурки внутри и снаружи. 5.Покрасочные работы. 6. Приобретение икон, церковной утвари и колоколов. Местные жители, бывшие прихожане, чьи души, истосковались без духовной пищи, начали сбор пожертвований. Пенсионеры Гавриил Куличик из д. Богданы и Федор Андреюк из д.Чижевщина жертвовали свой труд, работая на восстановлении святыни. Жабинковский РИК и торфобрикетный завод Гатча-Осовское на реставрацию церкви выделили по 5 тысяч рублей. Колхоз – агрофирма «Рогознянский» оказал помощь в распиловке лесоматериала. В то время еще действовала планово–распределительная система, поэтому возникали проблемы с приобретением материалов. Часть оцинкованной жести выделил райисполком, остальную жесть о. Иоанн искал в разных местах. О. Иоанн не только руководил строителями, но сам  покрывал купола оцинкованной жестью.
        </p>
        <p class="indent-8 py-2">Так выглядела Св.Владимировская церковь после восстановительных работ.
        </p>
        <p class="indent-8 py-2">
            <img src="{{asset('images/doc8.jpg')}}" class="#"/>
        </p>
        <p class="indent-8 py-2">В 2002 году настоятелем Св.Владимировской церкви назначен священник Анатолий  Пучинский. Не все было выполнено во время реставрации храма. Не было колоколов, недостаточно было церковной утвари. Отопление храма осуществлялось «печкой–буржуйкой» с выведенной в окно трубой. Прилегающая территория не была благоустроена. Дорога к храму оставалась без какого-либо покрытия, вся в рытвинах и ухабах. Эти проблемы и повышение благолепия храма пришлось решать о. Анатолию. На второй год службы им были заказаны три колокола. На колоколах были отлиты имена жертвователей: Николая, Михаила и Владимира. На престольный праздник 28 июля 2006 года колокола созывали своим звоном прихожан, многочисленных гостей и паломников на торжественное богослужение.
        </p>
        <p class="indent-8">В настоящее время выполнено благоустройство церковной территории. Для отопления храма подведен природный газ. Много делается по благолепию храма.
        </p>
        <p class="indent-8">Св.Владимироская церковь 28 июля 2016 года во время празднования престольного праздника в честь Св. Равноапостольного князя Владимира.
        </p>
        <img src="{{asset('images/doc9.jpg')}}" class="#"/>
        <p class="indent-8 py-2">Храм по плановому решению и объемно-пространственной композиции относится к типу кресто-купольных храмов с башней-колокольней на главном фасаде. Основной храмовый объем имеет в плане форму креста. По продольной оси к нему пристроены: с запада – башня-колокольня, с востока – пятистенная апсида. Сооружение завершается полусферичным куполом на массивном восьмигранном световом барабане, грани которого с полуциркульным завершением прорезаны высокими полуциркульными окнами. Над куполом возвышается головка на высокой шейке.
        </p>
        <p class="indent-8">Аналогичный купол завершает и башню-колокольню над притвором. В интерьере храма выделяется подкупольное пространство. 4 столба, размещенные в центре, являются основой для подпружных арок, поддерживающих купол на парусах.
        </p>
        <p class="indent-8">Церковь является памятником архитектуры неорусского стиля.
            28 июля 1990 года освящение совершил настоятель Свято-Симеоновского собора г. Бреста, секретарь Брестской епархии протоиерей Евгений Парфенюк.
        </p>
        <img src="{{asset('images/doc10.jpg')}}" class="#"/>
        <h3 class="text-2xl py-2 font-semibold text-center">Биография протоиерея Никанора Котовича</h3>
        <p class="indent-8 py-2">Никанор Антонович  Котович родился 26 августа (ст. ст.) 1845 года в селе Черевачицы Кобринского уезда. В 1859 году окончил Кобринское Духовное училище, а в 1865 году – Литовскую Духовную семинарию. С июня 1865 года Никанор Котович работал надзирателем Жировичского Духовного училища, а с мая 1866 года – учителем Кобринского Духовного училища. В начале 1867 года он по состоянию здоровья увольняется с училища. После лечения работал учителем Виленского Духовного училища. 18 октября 1871 года  в Вильне Никанор Котович рукоположен во священника и направлен на приход Хотиславской  Спасо – Преображенской церкви Брестского уезда. В 1874 году Никанор Котович переводится в Рогознянскую Кресто – Воздвиженскую церковь Кобринского уезда. Церковь была весьма ветхой, поэтому о.Никанор начал сбор средств на её ремонт. Осенью 1883 года о. Никанор переводится на освободившееся место на Крупчицкий приход, оставив 3 тыс. рублей своему преемнику. Имевшаяся там церковь Александра Невского сгорела до его прибытия. О. Никанор служит в приписной Мыщицкой Св. Успенской церкви. В Крупчицах он строит жилой дом с хозяйственными постройками и собирает средства на строительство новой каменной церкви – памятника о погибших воинах в сражении 1794 года, столетие которого приближалось. Стараниями о. Никанора в Крупчицах была построена каменная церковь, которая была освящена 10 июля 1894 года с именем равноапостольного князя Владимира Крестителя Руси.
        </p>
        <p class="indent-8">
            В Мыщицах о.Никанор является законоучителем в церковно – приходской школе. Также он занимается публицистической деятельностью. В Литовских Епархиальных Ведомостях печатались его проповеди, поучения и беседы, которые выходили отдельными брошюрами:
        </p>
        <ol class="list-decimal pl-8">
            <li>Беседа в день пятидесятилетия уничтожении унии.</li>
            <li>Поучения простому народу во 2-ю и 7-ю недели по пятидесятице.</li>
            <li>Поучения простому народу в день сошествия Святаго Духа и в неделю всех святых.</li>
            <li>Беседа к простому народу.</li>
        </ol>
        <p class="indent-8 py-2">О. Никанор составил простонародный катехизис на местном языке. Простыми людьми он прекрасно усваивался.
        </p>
        <p class="indent-8 py-2">Печатался он также в газете  «Воскрестное время», выходившейся в Варшаве. О.Никанор увлекался историей края, им собрана большая коллекция исторических документов по истории православных церквей. (к сожалению, во время 1-й Мировой войны многое погибло) Он занимался не только духовным просвещением своей паствы, но и светским. Им была открыта народная библиотека в Крупчицах, насчитывающая около 1500 книг. Жил он трудами и заботами об улучшении быта своих пасомых, путем проведения осушительных работ в крае. Он с братом Фомой и другими священниками подписали прошение Министру Государственных имуществ о проведении осушительных работ. Что было сделано в 1892 году. Его докладная записка о состоянии экономики края и путях её повышения, адресованная губернскому инженеру Габерману свидетельствует о всесторонней эрудированности о.Никанора Котовича. Осуществиться этим планам не суждено было сбыться из-за 1-й Мировой войны.
        </p>
        <p class="indent-8">Отец Никанор, как и его братья, был западным русистом, он был против навязанной Автокефалии, для православной церкви в Польше.
        </p>
        <p class="indent-8">С августа 1915г по июнь 1918 года находился в беженцах в России. С июля 1918 года по декабрь 1936 года  о. Никанор служил в Крупчицкой Св. Владимировской церкви.
        </p>
        <p class="indent-8">За свои труды о.Никанор награжден орденом Святой Анны 3-й степени. А вот от сана протоиерея, благословленного 19 июля 1921 года  Московским Патриархом Тихоном, отказался. В тяжелое время для церкви о. Никанор не думал о наградах. Его бескомпромиссность и честность не всегда нравилась начальству, о чем свидетельствует тяжба с Полесской Духовной Консисторией.
        </p>
        <p class="indent-8">Почил о. Никанор от земной жизни 1 апреля (ст. ст.) 1938 года. Похоронен на церковном погосте рядом с матушкой урожденной Романской Олимпиадой Львовной (4.03.1853- 22.12 1925)
        </p>
