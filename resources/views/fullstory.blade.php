        <h3 class="text-2xl py-2 font-semibold text-center">ПРИПИСНЫЕ К СВ.ВЛАДИМИРОВСКОМУ ХРАМУ ЦЕРКВИ
            МЫЩИЦКАЯ СВ.УСПЕНСКАЯ ЦЕРКОВЬ
        </h3>
        <p class="indent-8 py-2">Из Гродненского православно-церковного календаря 1899 года: « В с. Мыщицах, Кобринского уезда, Кобринского благочиния, в приписной к Крупчицкой приходской Владимировской церкви. Церковь деревянная с такой же колокольней. Когда и кем сооружена, по давности, неизвестно. Отстоит от приходской в 4 верстах.»
        </p>
        <p class="indent-8 py-2">В этой церкви служили священники: Евстафий Маркевич в 1858-1882гг, Никанор Котович 1882-1915гг. В 1915 году о.Никанором из Мыщицкой церкви в Россию были эвакуированы 5 колоколов весом от 15 до 2 пудов. Во время 1-й Мировой войны Св.Успенская церковь была повалена бурей. Возвратившись на приход в 1918 году о.Никанор занимался восстновлением Крупчицкой Св.Владимировской церкви. На восстановление Мыщицкой церкви не хватало ни сил, ни средств. Таким образом Мыщицкая в.Успенская церковь прекратила свое существование.
        </p>

