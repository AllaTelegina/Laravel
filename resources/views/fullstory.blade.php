            <p class="text-justify indent-8 py-2 p-8 font-semibold">Прощение</p>
            <p class="text-justify indent-8 py-2 p-8">Имеем честь почтительнейше доложить Вашему Преосвященству нижеследующее. В минувшую войну с немцами наш Крупчицкий приход подвергся ужасному разгрому и опустошению, потому что через него пролегало шоссе, по которому проходили бесчисленные русские, германские и австрийские войска, которые всё предавали пламени, разоряли и опустошали. В огне погибли почти все наши сёла и деревни, всё наше имущество. Мы сами поголовно выехали в Россию и скитались там несколько лет, терпя всякие лишения. Единственным нашим утешением и радостью было то, что среди общей гибели уцелел один наш св. храм (хотя и он сильно пострадал от немцев). На постройку этого храма в 1891-94 гг. мы, прихожане, вложили бесчисленную массу своего труда, сил, времени и денег.</p>
            <p class="text-justify indent-8 py-2 p-8">После постройки непрестанно поддерживали его в должном порядке, ремонтировали, украшали ценными предметами. А теперь мы узнаём, что предполагается передать нашу церковь р. католикам, которые ни одного даже гроша не дали, и пальцем не шевельнули при его постройке, которых вовсе даже не имеется в нашем приходе. Такая весть о передаче нашей церкви католикам до глубины души волнует всё население нашего прихода, которое остаётся, в таком случае, без всякой церкви и без пастырского руководства, и это в такое время, когда баптизм неудержимо разливается повсюду. До сих пор мы всегда были твёрдо преданны своей вере, но если в нынешнее тяжёлое время нас оставят на произвол судьбы - то, несомненно, многие и многие соблазнятся! Посему покорнейше просим Ваше Преосвященство заступиться за нас, перед кем следует, и сохранить нам наш храм.</p>
