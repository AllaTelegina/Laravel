        <h3 class="text-2xl py-2">10.Крупчицкая церковь  1943- 1969гг</h3>
        <p class="indent-8 py-2">
            Указом №520 от 22 октября 1943 года епископа Брестского и Кобринского Иоанна (в миру Иван Филиппович Лавриненко 1899-1985гг) на Крупчицкий приход был назначен священник Петр Львович Калишевич. ГАБО ф.210 оп.1 д.8 л.4
        </p>
        <p class="indent-8 py-2">
            Послужной список Петра Львовича Калишевича
        </p>
        <p class="indent-8 py-2">П.Л. Калишевич родился в 1893 году в селе Мыщицы в семье псаломщика. Находясь в России в беженцах в 1916 году, Петр Калишевич окончил учительскую семинарию в Тихоновской Пустыне Калужской губ. В 1921 году при Пинской Духовной Консистории сдал экзамен на псаломщика и назначен псаломщиком к Буховичской церкви Кобринского уезда. С1924 по 1943 год служил псаломщиком в Черняковской церкви Березовского района. 23 сентября 1943 года сдал экзамен на священника при Брестской Духовной Консистории. Указом епископа Бресткого и Кобринского Иоанна за №520 от 22 .10. 1943 года  назначен на Крупчицкий приход. Здесь он прослужил  до мая 1969 гола. Петр Калишевич в браке с Анной Федоровной урожденной Кот.
        </p>
        <p class="indent-8 py-2">8 мая 1945 года составлен договор о передаче Жабинковским райисполкомом здания Крупчицкой церкви в безсрочное и бесплатное пользование общиной. Договор подписали: секретарь исполнительного комитета Жабинковского Совета депутатов трудящихся  Вершинин Борис Михайлович. От прихода подписали 26 прихожан, первый подписант Карпинчик Максим Степанович. Тогда же была составлена опись церковного имущества с описанием строительства церкви. В июне 1945 года была зарегистрирована церковная община  в Уполномоченного по делам Русской Православной церкви при Облисполкоме. Так же были зарегистрированы церковный Совет и ревизионная комиссия с анкетами на всех членов. В дальнейшем все изменения по составу указанных церковных органов регистрировались в уполномоченного. 18 октября 1951 года был составлен акт Следующего содержания:  «Мы, нижеподписавшиеся члены комиссии по техническому осмотру церквей в составе: Благочинного Жабинковского округа протоиерея Владимира Рункевича, настоятеля Крупчицкой церкви священника Петра Калишевича, техника Жабинковского Райккомхоза Ковальчука Бориса Михайловича, а также членов Церковного Совета Крупчицкой церкви Ящука Александра Романовичаи Ципринюка Евфимия Адамовича, действуя на основании распоряжения Минского Епархиального от 5.6.1951г №793 произвели техническое обследование здания Крупчицкой Св.Владимировской церкви, результаты коего нижеследующие: Храм каменный, построен в 1894г. в 1951 капитально отремонтирован. Состояние храма хорошее. Подписи. ГАБО ф.210 оп.1 д.8 л.1-39
        </p>
        <p class="indent-8 py-2">После войны прихожане Св. Владимировской церкви с ее настоятелем о.Петром Калишевичем, несмотря на материальные трудности, приобретали церковные предметы: в1949году приобрели иконы Святой Троицы, Рождества Христова, Успения Пр. Богородицы, Крещения Господня каждая по 240 рублей. В1948-1950гг приобретены 4 ризы с подризниками, приобретен новый Антиминс Священнодействован Высокопреосвященным Питиримом Митрополитом Минским и Белорусским. Приобретено Паникадило 2-х ярусное на 18 свечей медное посеребренное, облачения престола и жертвенника. В 1950-1951гг произведен ремонт церкви: Выполнен ремонт штукатурки стен внутри и снаружи, все стены храма выбелены известью, а внутри в рост человека покрашены масляной краской, В средней части церкви выложен новый пол и сделан клирос. На ремонт храма израсходовано 15000 рублей из церковной кассы и пожертвований прихожан. В1956 году за 2 тыс. рублей приобретена Голгофа с предстоящими Пресвятой Богородицы и Св.Апостола Иоанна Богослова, а также за 1 тыс. рублей приобретены 4 иконы Страдание Господа нашего Иисуса Христа.
