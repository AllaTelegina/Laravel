        <h3 class="text-2xl py-2">9. Св.Владимировская церковь 1938 – 1943гг</h3>
        <p class="indent-8 py-2">
            Намеченный о. Никанором ремонт Крупчицкой Св.Владимировской церкви, из-за недостатка средств и отправкой его за штат, а также из- за того, что с апреля 1938года  до 11 ноября 1939 года в Крупчицкой церкви сменилось четыре священника ремонт церкви не был выполнен. Из той же книги описи церковного имущества известно, что в 1939 году для церкви были приобретены две посеребренные лампадки изящной работы, установленные у икон «Тайная вечера» и запрестольной выносной иконой Равноаппостольного князя Владимира и на обратной стороне Пресвятой Богородицы. Приобретены две ризы и панихидное блюдо. На пожертвования прихожанина Кондратия Андреюка приобретен пасхальный посеребренный трехсвечник, а на пожертвования Степана Карпинчика приобретены иконы Божией Матери и Николая Чудотворца. В то время в Крупчицкой церкви были иконы в основном писаные на холсте. Фольговых икон имелось четыре, на металле – четыре, а также четыре иконы Евангелистов, находящихся в « Царских вратах».
        </p>
        <p class="indent-8 py-2">
            9 апреля 1939 года во время пасхального Богослужения в Крупчцкой церкви произошло такое событие. В церковь пришла молодежь, находясь  в притворе они курили, громко разговаривали, смеялись. Настоятель прихода Владимир Антипорович, прервав Богослужение, с помощью прихожан удалил их из храма. После смены власти в сентябре того же года эта молодежь, а это были комсомольцы, оказались при власти и с оружием. Они начали мстить о.Владимиру и всячески издеваться над ним. Ночью человек 20 ворвались в квартиру настоятеля. Угрожая оружием стали требовать отдать оружие, коего у него никогда не было. С бранью, нецензурными словами, они стали производить обыск в квартире, переписывая личные и церковные вещи, грозя арестовать его. Забрав урожай и сено они поселили в церковный дом две семьи. 15 ноября 1939 года написав рапорт благочинному 1-го Кобринского округа о невозможности дальнейшего пребывания его на Крупчицком приходе о.Владимир передал церковь настоятелю Рогознянской церкви Николаю Михаловскому. Священник Владимир Антипорович с семьей переехал в с.Токари Брестского района, где служил настоятелем его отец. См. личное дело священника Владимира Антипоровича. ГАБО ф.2059 оп.5 д.6 л.1-42
        </p>
        <p class="indent-8 py-2">
            В конце 1941 года Архиепископом Пинским Александром (Иноземцевым) был рукоположен во священника и назначен на  Крупчицкий приход псаломщик Клеванской церкви Ровенской области Иоанн Трофимович Серветник.
        </p>
