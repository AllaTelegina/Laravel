            Весной 1943 года, когда в селе Крупчицы была сильная буря, ночью в квартиру Иоанна Серветника ворвались неизвестные вооруженные люди, (по рассказам местных жителей это были партизаны). По неизвестным причинам, они тяжело ранили Иоанна Серветника, который скончался в Кобринской больнице 4 мая 1943 года. О смерти священника Иоанна Серветника сообщается в циркулярном письме от 21 мая 1943 года благочинного 3-го  Кобринского округа Василия Геннадьевича Васильева настоятеля Дивинской Параскевы-Пятницкой церкви.
