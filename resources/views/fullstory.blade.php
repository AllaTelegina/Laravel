        <p class="indent-8">
            Освящение вновь построенной церкви совершено 10 июля 1894 года Виленским Кафедральным
            протоиереем Иоанном Котовичем при участии 16 священников и двух дьяконов при стечении
            большого количества местного населения.
        </p>

        <h3 class="text-2xl py-2">5. Крупчицкая Св.Владимировская церковь до I-й Мировой войны</h3>
        <p class="indent-8">
            На строительство церкви были израсходованы все выделенные средства. Церковь была построена,
            но она имела самую скудную утварь. О. Никанор продолжал собирать средства с разных мест.
            Жертвовал церковной утварью уже упоминаемый Священник Иоанн Кронштадский (в миру Иван
            Ильич Сергиев 1829-1909). Неизвестный жертвователь подарил серебрянно-позолоченную чашу
            с эмалью весом 5 фунтов (1 фунт = 0,4095 кг.). Бывшие прихожане, проживающие в США,
            прислали около тысячи долларов, на которые были куплены 2 пары металлических хоругвий,
            такой же запрестольный крест и выносной образ с киотом, прекрасно написанный по особому
            заказу. К началу 1-й Мировой войны имелось 15 комплектов облачений. Каждые 4-5 лет
            производился ремонт церкви. Красилась масляной краской крыша, исправлялась штукатурка,
            белились стены и т.д. Перед войной Крупчицкая церковь стала одной из самих благоустроенных
            в епархии. В последний раз церковь ремонтировалась в 1912</p>
        <p class="indent-8 py-4">
            Из Гродненского православного церковного календаря: «В с. Крупчицах, Кобринского уезда, Кобринского благочиния в приходской церкви. Церковь каменная с такой же колокольней, в деревянной ограде. Сооружена в 1894 году, средствами Правительства, после бывшей там деревянной Александра – Невской церкви, сгоревшей 9 сентября 1882 года. Прихожан в ней 764 муж. 745 женского пола в 9 селениях: Крупчицах, Богданах, Мыщицах, Паевщине, Перках, Рыковичах, Томашках, Ходосах и Чижевщине. Имеется: Приписная Успенская церковь в Мыщицах, братство, церковно-приходское попечительство. Церковная школа в Мыщицах с 1886г с 10 учащихся мальчиков. Земли в пользовании причта: усадебной 4 десятины, пахотной-33дес, сенокосной - 8дес., пастбищной - 17дес., неудобной - 16дес. (1дес.=1,066га).
        </p>
        <img src="{{asset('images/Suvorov.png')}}" class="py-4"/>
        <p class="indent-8 py-4">
            Эта памятная доска была установлена в Крупчицкой церкви. В 1915 году она вместе с другим церковным имуществом о.Никанором была эвакуирована в Россию.
        </p>

        <h3 class="text-2xl py-2">6. I-я Мировая война</h3>
        <p class="indent-8">
            Летом 1915 года нависла угроза оккупации края немецкими войсками. По приказу Верховного
            командования российской армии церковное имущество вывозилось в Россию. Выполняя этот
            приказ о.Никанор с прихожанами собрали наиболее ценные церковные предметы. Уложили
            их в сундук и 4 деревянные ящики и тремя подводами доставили на ст.Кобрин. Груз был
            адресован на ст. Снитово Полесской железной дороги. Вес составлял 28 пудов 10 фунтов
            (1пуд - 16,38кг, 1фунт - 0,4095кг) багажная квитанция № 1361 от 12 августа 1915 года.
            На ст.Жабинка были отправлены 4 колокола из Крупчицкой и 5 из Мыщицкой Св.Успенской
            церкви весом от 15 до 2 пудов. Сребро – позолоченная чаша о.Никанором была передана
            в Донской монастырь г. Москвы. В последнюю ночь перед эвакуацией о.Никанор с церковным
            старостой часть церковного имущества закопали. В России о.Никанор с матушкой Олимпиадой
            находились в Воронежской губернии. В конце 1915года за ними приехал младший сын Зиновий
            и перевез их в Петроград, где они жили у своих детей. В мае 1918 года о. Никанор с
            матушкой переехали в уездной город Речица Минской губернии.
        </p>

        <h3 class="text-2xl py-2">7. Восстановление Православия в Западном крае</h3>
        <p class="indent-8">
            В июне 1918 года о.Никанор с матушкой Олимпиадой возвращается из Речицы, в теперь родные, Крупчицы. Здесь он увидел удручающую картину. Св.Владимировская церковь, некогда одна из лучших церквей благочиния оказалась в запустении: замки с дверей сорваны, на колокольне окна выбиты, (во время боевых действий немцы устроили там наблюдательный пункт) на колокольне и в отдельных местах церкви пол прогнил, кровля пробита пулями. В церковном доме жили евреи, которые были выселены немецкими властями из Бреста. Из 6 деревень прихода 3 сгорели полностью, четвертая наполовину. В селе Крупчицы осталось несколько уцелевших домов, малочисленные крестьяне ютились в землянках. Поля не обрабатывались и заросли бурьяном кустарником и лесом.
        </p>
        <p class="indent-8">
            О.Никанору немецкие власти поручили заведовать Крупчицким, Озятским Рогознянским и Бульковским приходами. Не было облачений, церковной утвари и церковных предметов для проведения богослужений. Некоторые предметы пришлось взять из других церквей. Как вспоминал о.Никанор в крае свирепствовал тиф.
        </p>
        <p class="indent-8">
            Являясь ревнителем Церкви Христовой, о.Никанор, с присущей ему энергией, стал разыскивать отправленные в Россию церковные вещи, а это были:
        </p>
        <ol class="list-decimal pl-8">
            <li>Евангелие новое 1894г Московского издания.</li>
            <li>Евангелие 1874г Московского издания.</li>
            <li>Крест напрестольный позолоченный – 2 шт.</li>
            <li>Хоругви металлические позолоченные – 4 шт. </li>
            <li>Дарохранительница высокохудожественной работы посеребрянная -1 шт.</li>
            <li>Сосуд для освящения хлебов, чаша, копье, звездица -3, лжица -3, кадильница все с благородным металлом.</li>
            <li>Иконы – 7 шт.</li>
            <li>Облачения священнические – 16 комплектов.</li>
            <li>Ковры 5-1,2м 2 шт.</li>
            <li>Ковровая дорожка- 3 шт.</li>
            <li>Около десятка кусков шелковой материи для починки риз.</li>
            <li>Медная позолоченная доска с выгравированною надписью в память о Суворове весом около 20 фунтов.</li>
            <li>Около 1000 экземпляров книг из Павленковской библиотеки.</li>
            <li>Церковный капитал 436 рублей находившийся в Кобринском Уездном Казначействе.</li>
        </ol>
        <p class="indent-8">
            По вопросу возврата церковных вещей о.Никанор обращался в различные учреждения. Из Центрального Бюро эвакуированных вещей пришел ответ, что багаж был выгружен на ст. Жудилово Полесской железной дороги, сундук, 2 ящика и корзина, что не соответствовало отправленному багажу. Вернуть церковное имущество не удалось, так как в России произошли две революции, а затем Гражданская война.
        </p>
        <p class="indent-8">
            В1953 году памятная доска из музея г. Смоленска была переслана в музей А.В.Суворова г.Кобрина, где хранится в настоящее время.
        </p>
        <p class="indent-8">
            К концу 1918 года немецкие войска ушли, на смену им пришли польские. Началась польско–советская война, проходившая с переменным успехом. В 1920 году Красная Армия потерпела поражение. По Рижскому мирному договору часть территории Западного края отошла к Польше. На отошедшей территории оказались 3 епархии: Виленская, Гродненская (Белостокское воеводство) и Полесская (Полесское и Новогрудское воеводство). Две области Западной Украины – Волынь и Холмщина. В не простых условиях оказались православные епархии, в стране с преобладанием католического населения, к тому же не дружественной к России. Польские власти все сделали, для того чтобы Православную Церковь в Польше оторвать от Московского Патриархата. Для этого за Православной Церковью установили жесткий контроль со стороны светской власти  и навязали автокефалию. В 1924 году польские власти ввели новый стиль в церковное богослужение. Прихожане приходили в церковь в праздники по старому стилю. Полиция их разгоняла, что вызывало волнения и нестроения местного населения. Властям  пришлось отменить этот указ.
        </p>
        <h3 class="text-2xl py-2">8. В составе Полесской Епархии</h3>
        <p class="indent-8">
            Противника Автокефалии Архиепископа Пинского и Новогрудского Пантелеимона (в миру Павел Степанович Рожновский 1867-1950гг) польские власти лишили кафедры, отправили на покой и заточили в Жировичский монастырь. 12 июня 1922 года на освободившуюся кафедру назначили епископа Александра (в миру Николай Иванович Иноземцев 1887-1948), в конце года епископ Пинский и Полесский. В конце 1925 года епархия стала называться Полесская и Пинская. 3 июня 1927года епископ Александр возведен в сан Архиепископа. Митрополитом Варшавским и Ковельским и всея Польши был избран архиепископ Волынский Дионисий (в миру Константин Николаевич Валединский 1876-1960).
        </p>
        <p class="indent-8">
            Церкви Черевачицкого благочиния, куда входила Крупчицкая Св.Владимировская церковь, входили во 2-й благочинный округ Кобринского уезда. Кобринские благочиния входили в состав Полесско-Пинской епархии.
        </p>
        <h3 class="text-2xl py-2">8. В составе Полесской Епархии</h3>
        <p class="indent-8">
            Противника Автокефалии Архиепископа Пинского и Новогрудского Пантелеимона (в миру Павел Степанович Рожновский 1867-1950гг) польские власти лишили кафедры, отправили на покой и заточили в Жировичский монастырь. 12 июня 1922 года на освободившуюся кафедру назначили епископа Александра (в миру Николай Иванович Иноземцев 1887-1948), в конце года епископ Пинский и Полесский. В конце 1925 года епархия стала называться Полесская и Пинская. 3 июня 1927года епископ Александр возведен в сан Архиепископа. Митрополитом Варшавским и Ковельским и всея Польши был избран архиепископ Волынский Дионисий (в миру Константин Николаевич Валединский 1876-1960).
        </p>
        <p class="indent-8">
            Церкви Черевачицкого благочиния, куда входила Крупчицкая Св.Владимировская церковь, входили во 2-й благочинный округ Кобринского уезда. Кобринские благочиния входили в состав Полесско-Пинской епархии.
        </p>
        <p class="indent-8">
            Назначение священников на приходы осуществлялось по согласованию с местными властями. Священники должны были иметь польское гражданство, которое выдавалось министерством внутренних дел. Также священники подписывали согласие об автокефалии и присягу на верность польскому государству. Позднее польские власти предприняли через судебные иски возвратить и передать католикам церкви, которые перешли в православие после упразднения унии.
        </p>
        <div class="bg-orange-50 border-2 border-blue-950 m-24 p-4">
            <p class="text-justify indent-8 py-2 p-8">Сообщаю, что православный священник Никанор Котович, исполняющий душпастырские обязанности в филиале в Крупчицах, вызванный для подписания декларации за автокефалию православной церкви в Польше - отказался от подписания этой - объясняя, что в этом деле должен посоветоваться со своими духовными властями.</p>
            <p class="text-justify indent-8 py-2 p-8">Прошу ответить, может ли быть выдан Котовичу документ о гражданстве?</p>
            <p class="text-justify indent-8 py-2 p-8">Со своей стороны вношу предложение об освобождении православного священника Никанора Котовича от выполнения душпастырских функций, поскольку не признал автокефалию православной церкви в Польше, не может он быть священником и даже прихожанином этой церкви.</p>
            <p class="py-4 pl-8">Кобринский староста Ф. Андрацкий21</p>
        </div>
        <p class="indent-8">
            Поняв бесполезность борьбы, Никанор Котович 2 мая 1925 года подписал декларацию о признании автокефалии церкви и дал объяснение - почему не подписал её раньше.
        </p>
        <div class="bg-orange-50 border-2 border-blue-950 m-24 p-4">
            <p class="indent-8">Протокол,</p>
            <p class="text-justify indent-8 py-2 p-8">написанный 2 мая 1925 года в правлении Кобринского староства с православным священником Никанором Котовичем, в деле подписания декларации за автокефалию православной церкви в Польше.</p>
            <p class="text-justify indent-8 py-2 p-8">Опрошенный в вышеуказанном деле священник Котович заявил следующее:</p>
            <p class="text-justify indent-8 py-2 p-8">При первом вызове меня в Кобринское староство отказался подписывать декларацию за автокефалию православной церкви в Польше, так как в других уездах Полесского воеводства, например, в Дрогичинском уезде не требовали от моего брата Василия Котовича, настоятеля православного прихода Ополь. Кроме того, не имел официального распоряжения от духовных властей в обязанности поддержки автокефалии православной церкви в Польше.</p>
            <p class="text-justify indent-8 py-2 p-8">Такое распоряжение после моего первого посещения староства было помещено в митрополитальном журнале "Вестник Варшавской Митрополии" и в еженедельнике "Воскресное Чтение", поэтому сейчас соглашаюсь подписать вышеуказанную декларацию.
            </p>
            <p class="text-justify indent-8 py-2 p-8">
                Правдивость моего вышеуказанного признания после прочитания подтверждаю.
            </p>
            <p class="py-4 pl-8">Никанор Котович22</p>
        </div>

        <p class="indent-8">
            В 1925 году над православными церквями в Польше нависла  угроза.</br>
            «Указ Полесской Духовной Консистории о.Благочинному 2-го округа Кобринского уезда Протоиерею о.Ионну Михаловскому. По сведениям имеющимся в распоряжении Консистории, Пинским Римско-Католическим епископом Ловинским предъявлены в Пинском Окружном Суде определения в г Брест иски к Полесской Духовной Консистории о передаче Римско-Католическому духовенству церковных земель в следующих приходах вверенного Вам округа: 1.Здитово, 2.Сехновичи, 3.Мыщицы, 4.Озяты, 5.Степанки, 6.Рогозна. 7.Бульково, 8.Черевачицы, 9.Крупчицы, 10.Вежки, 11.Тевли. Иск основан на том соображении, что имущество это принадлежало в свое время бывшим униатским и католическим приходам.
        </p>
        <p class="indent-8">
            Во исполнение определения Духовной  консистории от июля сего года за №110 Утвержденного резолюцией Его Высокопреосвященства от того же числа за №1762 предлагается Вам:
        </p>
        <p class="indent-8">
            1.Объявить духовенству вышепоименованных приходов об опасности угрожающей существовании сих приходов и поручения о.о.настоятелям исследовать на местах исторические материалы и документы касающихся сих православных приходов и строго держаться имущества, если таковые исторические материалы и документы имеются в церковных архивах или у прихожан. О результатах обследования предоставить в Консисторию мотивированные доклады с приложением соответствующих документов, справок и списков свидетелей, показания коих могут иметь для дела значение».
        </p>
        <p class="indent-8">
            Копии этого указа были разосланы по всем приходам.
        </p>
        <p class="indent-8">
            На этот период в благочинии были только два священника служивших до 1-й Мировой войны. Это Иоанн Михаловский и Никанор Котович. Являясь эрудированным священником, Никанор Котович  писал исторические очерки о здешнем крае, поэтому он предоставил в Консисторию документы, подтверждающие принадлежность указанных приходов к православию. Также были прошения прихожан в Полесскую Консисторию.
        </p>
        <div class="bg-orange-50 border-2 border-blue-950 m-24 p-4">
            <p class="py-2 pl-16">Его Преосвященству</br>
